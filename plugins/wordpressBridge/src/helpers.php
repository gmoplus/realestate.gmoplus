if (!function_exists('wbMake')) {
    /**
     * Get instance of the 'rl' prefix based Flynax class
     *
     * @param string $className - Flynax class name
     *
     * @return bool|mixed - Instance of the provided class or false
     */
    function wbMake($className)
    {
        if (!$className) {
            return false;
        }

        return ObjectsContainer::i()->make($className);
    }
